function fizzBuzz(n) {
  for(var num = 1; num < 16; num++){
    var check3 = num % 3;
    var check5 = num % 5;
    if((check3 == 0 && check5 == 0)){
      console.log('fizzbuzz');
    }else if(check3 == 0){
      console.log('fizz');
    }
    else if(check5 == 0){
      console.log('buzz')
    }else
    console.log(num)
  }
}

